 <h2>Redes Bimodais Artistas-Tema</h2>
 <div class="container">
     <div class="columns">
         <div class="column col-6">
             <p>As redes bimodais aqui presentes foram criadas a partir dos(as) artitas-temas das críticas de arte de cada uma das cinco revistas do corpus e dos(as) autores(as) que escreveram sobre eles(elas). Assim, uma crítica de arte que trata de uma exposição coletiva gera a conexão de seu(sua) autor(a) com os nomes dos(as) artistas mencionados em seu texto e que tiveram presentes na mostra. No caso de um(a) autor(a) que escreveu várias críticas, seu nome será vinculado com o de todos(as) os(as) seus(suas) artistas-tema.</p>
             <p>As cores dos nós correspondem ao país de nascimento dos(as) artistas-tema e dos(as) autores(as), em azul os(as) nascidos(as) no Brasil e em cinza aqueles(as) sobre os quais não foi possível identificar essa informação. Os(as) autores(as) são representados pela forma de losangos e os(as) artistas-tema pela forma de elipses. Para a criação das redes não foram consideradas as críticas escritas exclusivamente sobre coletivos de artistas que não mencionam nomes de artistas individualmente.</p>

 <h2>Redes Bimodais</h2>
 <div class="container">
     <div class="columns">
         <div class="column col-6">
             <p>As redes bimodais aqui presentes foram criadas a partir dos artitas-temas das críticas de arte de cada uma das cinco revistas do corpus e dos autores que escreveram sobre eles. Assim, uma crítica de arte que trata de uma exposição coletiva gera a conexão de seu autor com os nomes dos artistas mencionados em seu texto e que tiveram presentes na mostra. No caso de um autor que escreveu várias críticas, seu nome sera vinculado com o de todos os seus artistas-temas.</p>
             <p>As cores dos nós correspondem ao país de nascimento dos artistas-temas e dos autores, em azul os  nascidos no Brasil e em cinza-claro aqueles sobre os quais não foi possível identificar essa informação. Os autores são representados pela forma de losangos e os artistas-temas pela forma de elipses. Para a criação das redes não foram consideradas as críticas escritas exclusivamente sobre artistas coletivos.</p>

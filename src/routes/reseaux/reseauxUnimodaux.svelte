<h2>Redes Unimodais Artistas-Tema</h2>
<div class="container">
	<div class="columns">
		<div class="column col-6">
			<p>As redes unimodais aqui presentes foram criadas a partir dos(as) artistas-tema das críticas de arte de cada uma das cinco revistas do corpus. Dois(duas) artistas são conectados entre si quando abordados por pelo menos um(a) autor(a). Assim, uma crítica de arte que trata de uma exposição coletiva gera a conexão dos nomes de artistas por ela mencionados e que tiveram presentes na mostra, pois foram trabalhados por um(a) mesmo(a) autor(a). No caso de um(a) autor(a) que escreveu várias críticas, todos(as) os(as) seus(suas) artistas-tema são conectados(as) entre si na rede.</p>
			<p>As cores dos nós correspondem ao país de nascimento dos artistas-tema, em azul os(as) artistas nascidos no Brasil e em cinza aqueles(as) sobre os(as) quais não foi possível identificar essa informação. Para a criação das redes não foram consideradas as críticas escritas exclusivamente sobre coletivos de artistas que não mencionam nomes de artistas individualmente.</p>
			<p>Você também pode acessar uma <a href="/reseaux/reseauxUnimodauxEIC">segunda versão</a> dessas redes que mostra o resultado da pesquisa dos nomes dos artistas-tema de cada revista na <a href="https://enciclopedia.itaucultural.org.br/">Enciclopédia Itaú Cultural</a>.</p>

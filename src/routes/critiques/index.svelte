<h2>Críticas que compõem o corpus</h2>

<div class="container">
	<div class="columns">
		<div class="column col-6">
			<p>Para a criação do corpus foram selecionados textos publicados até abril de 2019 por cinco revistas de arte digitais atuantes no Brasil (<a href="https://www.iconica.com.br/">Icônica</a>, <a href="https://performatus.com.br/">eRevista Performatus</a>, <a href="https://artcontexto.com.br/">Arte Contexto</a>, <a href="https://revistadesvio.com/">Revista Desvio</a> e <a href="http://ofermentorevista.com.br/">o fermento</a>) segundo características gerais da crítica de arte, baseadas no ensaio <i><a href="https://www.cairn.info/la-critique-d-art--9782842925932.htm">La critique d'art</a></i> de Claire Fagnart (2017). São elas: as críticas de arte são textos escritos; esses textos analisam obras de arte ou eventos da atualidade de seus autores, a qual foi considerada a partir do ano 2000; esses textos não são um texto em hitória da arte e nem em teoria da arte; o autor da crítica não é o artista da obra discutida, nem o curador da exposição, nem o autor dos textos de apresentação da mostra. </p>
			<p>Para a identificação das críticas de arte publicadas por cada revista, os títulos dos textos e curtas apresentações foram de fundamental importância. As fichas técnicas presentes nas legendas de obras foram utilizadas como uma maneira de estabelecer o intervalo temporal trabalhado pelos autores. Além disso, foram identificadas as seções de cada revista que publicam habitualmente conteúdos relacionados com a crítica de arte (por exemplo: Icônica - <a href="https://www.iconica.com.br/site/paragem/">Paragem</a>; eRevista Performatus - <a href="https://performatus.com.br/categoria/perfil-de-artista/">Perfil de Artista</a>, quando não apresenta diretamente uma entrevista com o artista, e <a href="https://performatus.com.br/categoria/criticas/">Críticas</a>; Arte ConTexto - Aposta; Revista Desvio - <a href="https://revistadesvio.com/category/critica-semanal/">Crítica Semanal</a>; e o fermento - <a href="http://ofermentorevista.com.br/category/resenhas/">Resenhas</a>). No caso dos comentários críticos sobre eventos, foram consideradas as menções a artistas do passado quando esses estavam em exposição. Entre os eventos, foram considerados: exposições de arte, festivais de dança, de teatro, de cinema e de performance, mas também o momento do falecimento de artistas, quando esse motivou a escrita de reflexões críticas sobre as suas produções.</p>

<h2>Levantamento de revistas de arte digitais no Brasil</h2>

<div class="container">
	<div class="columns">
		<div class="column col-6">
			<p>Com o objetivo de melhor entender o universo das revistas digitais que abordam o tema da arte no Brasil, durante a minha pesquisa de doutorado, eu realizei um levantamento dessas publicações de modo a identificar suas características gerais. Nesse levantamento, informações que permitem o referenciamento bibliográfico dessas publicações - nome, site web, se a revista ainda está disponível ou não, data da primeira e última publicação consultada, cidade e estado - foram incluídas. Além disso, eu adicionei informações sobre o conteúdo dessas revistas, por exemplo, se elas publicam ou não em formato PDF ou impresso, e sobre os meios de divulgação. Também inclui os temas gerais de cada revista, identificados nos textos de apresentação e também a partir de uma consultação rápida dos conteúdos publicados. Os meios de financiamento identificados dessas revistas também foram incluídos no levantamento, de maneira não exaustiva, além de outras atividades que essas publicações realizaram, como exposições e oficinas.</p>
            <p>Nesse levantamento, que foi encerrado em agosto 2023, não foram incluídas as revistas digitais com gestão vinculada a universidades, de maneira a reduzir o escopo das revistas levantadas e se concentrar em iniciativas que tentam alcançar outros públicos.</p>
